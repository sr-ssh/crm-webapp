export function translate(key) {
    if (key === "addOrder")
        return "ثبت سفارش"
    else if (key === "getOrders")
        return "سفارش ها"
    else if (key === "reminder")
        return "یادآوری"
    else if (key === "getProducts")
        return "محصولات"
    else if (key === "finance")
        return "مالی"
    else if (key === "getCustomers")
        return "مشتریان"
    else if (key === "getEmployees")
        return "کارمندان"
    else if (key === "getDiscounts")
        return "تخفیف ها"
    else if (key === "family")
        return "نام"
    else if (key === "company")
        return "نام شرکت"
    else if (key === "address")
        return "آدرس"
    else if (key === "price")
        return "قیمت"
    else if (key === "quantity")
        return "تعداد"
}